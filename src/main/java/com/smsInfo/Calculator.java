package com.smsInfo;

public class Calculator {
	public int sum(int a, int b) {
		int c = a + b;
		return c;
	}
	
	public int Sub(int a, int b) {
		int c = a - b;
		return c;
	}
	
	public int Multi(int a, int b) {
		int c = a * b;
		return c;
	}
	
	public int Div (int a, int b) {
		int c = a / b;
		return c;
	}
	

}
